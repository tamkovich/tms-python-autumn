def sum_of_arg(a, b: float) -> float:
    """Function sums arguments"""
    return a + b


def subtraction_of_arg(a, b: float) -> float:
    """Function subtracts arguments"""
    return a - b


def mult_of_arg(a, b: float) -> float:
    """Function multiply arguments"""
    return a * b


def division_of_arg(a, b: float) -> float:
    """Function divides arguments"""
    return a / b
