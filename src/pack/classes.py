class Math:
    @staticmethod
    def sum_of_arg(a, b: float) -> float:
        """Method sums the arguments"""
        return a + b

    @staticmethod
    def subtraction_of_arg(a, b: float) -> float:
        """Method subtractions arguments"""
        return a - b

    @staticmethod
    def mult_of_arg(a, b: float) -> float:
        """Method multiplies arguments"""
        return a * b

    @staticmethod
    def division_of_arg(a, b: float) -> float:
        """Method divides arguments"""
        return a / b
